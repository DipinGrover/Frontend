// Relational operators
let x = 10;
let y = 20;
let z = 5;
let a = 10;

console.log("x < z" , x<z);
console.log("y > z" , y>z);
console.log("x <= z" , x<=z);
console.log("x >= a" , x>=a);


// Logical operators 
console.log(true && false);
console.log(false || false);
console.log(true && true);
console.log((5<10) && (6<3));
console.log(!false);

// short circuiting
console.log(6 && 7);
console.log(0 && -11);
console.log(4 || 8);
console.log(0 ||-13); 